#include <stdio.h>

int main() {
    int age = 15;

    printf("Greater than: %d", age > 10);
    printf("Smaller than: %d", age < 10);
    printf("Greater than Equal to: %d", age >= 10);
    printf("Smaller than Equal to: %d", age <= 10);
}

// relational operator 
// relational also give boolean value (1(true), 0(false))
// Greater than = >
// Smaller than = >
// Greater than Equal to = >=
// Smaller than Equal to = <=